"""Parameter recovery tests for spectral DCM (REC-02).

Validates that Pyro SVI inference correctly recovers known ground-truth
connectivity parameters (A matrix) from synthetic CSD data generated by
the spectral DCM simulator. Tests use the simulate -> infer -> compare
protocol.

Metrics:
- RMSE(A) < 0.05 averaged over 10 synthetic 3-region datasets
- 95% CI coverage in [0.80, 0.99] for A matrix elements
- Correlation(A_true, A_inferred) > 0.85

Note: Coverage range is [0.80, 0.99] rather than [0.90, 0.99] due to
known mean-field VI underdispersion (AutoNormal ignores posterior
correlations between A, noise_a, noise_b, noise_c parameters). See
RESEARCH.md Pitfall 1.

References
----------
[REF-010] Friston, Kahan, Biswal & Razi (2014). A DCM for resting
    state fMRI. NeuroImage, 94, 396-407. Eq. 3-10.
"""

from __future__ import annotations

import pytest
import torch
import pyro

from pyro_dcm.forward_models.neural_state import parameterize_A
from pyro_dcm.models import (
    spectral_dcm_model,
    create_guide,
    run_svi,
    extract_posterior_params,
)
from pyro_dcm.models.spectral_dcm_model import decompose_csd_for_likelihood
from pyro_dcm.simulators.spectral_simulator import (
    make_stable_A_spectral,
    simulate_spectral_dcm,
)


