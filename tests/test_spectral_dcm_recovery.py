class TestSpectralDCMRecoverySlow:
    """Spectral DCM parameter recovery -- slow validation tests."""

    @pytest.mark.slow
    def test_spectral_dcm_recovery_50_datasets(self) -> None:
        """50 datasets, 3 regions, 3000 steps. Full validation."""
        results = []
        for seed in range(200, 250):
            result = run_single_spectral_recovery(
                seed=seed, n_regions=3, num_steps=3000, snr=10.0,
            )
            if result is not None:
                results.append(result)

        assert len(results) >= 40, (
            f"Too few successful runs: {len(results)}/50"
        )

        rmses = [
            compute_rmse_A(r["A_true"], r["A_inferred"])
            for r in results
        ]
        mean_rmse = sum(rmses) / len(rmses)
        assert mean_rmse < 0.05, f"Mean RMSE {mean_rmse:.4f} >= 0.05"

        coverage = compute_coverage(results)
        assert 0.80 <= coverage <= 0.99, (
            f"Coverage {coverage:.4f} not in [0.80, 0.99]"
        )

        all_true = torch.cat([r["A_true"].flatten() for r in results])
        all_inferred = torch.cat(
            [r["A_inferred"].flatten() for r in results]
        )
        corr = _pearson_corr(all_true, all_inferred)
        assert corr > 0.85, f"Correlation {corr:.4f} <= 0.85"

