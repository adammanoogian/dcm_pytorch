class TestTaskDCMRecovery:
    """Task DCM parameter recovery -- CI-fast tests (REC-01).

    Validates that the SVI inference pipeline produces reasonable
    results on a small number of quick trials. Strict recovery
    thresholds are enforced in the slow validation tests.

    Uses 2 datasets x 200 SVI steps x 30s simulation to keep
    CI runtime under 5 minutes.
    """

    @pytest.fixture(scope="class")
    def quick_results(self) -> list[dict]:
        """Run 2 quick task DCM recovery trials."""
        results = []
        for seed in [100, 103]:
            result = run_single_task_recovery(
                seed=seed, n_regions=3, num_steps=200,
                duration=30.0, n_blocks=2,
            )
            if result is not None:
                results.append(result)
        return results

    def test_task_dcm_svi_completes(
        self, quick_results: list[dict],
    ) -> None:
        """SVI completes without NaN on at least 1 of 2 datasets."""
        assert len(quick_results) >= 1, (
            "All task DCM SVI runs failed (NaN/diverged)"
        )

    def test_task_dcm_loss_decreases(
        self, quick_results: list[dict],
    ) -> None:
        """ELBO loss decreases during SVI optimization."""
        assert len(quick_results) >= 1, "No successful runs"
        for i, r in enumerate(quick_results):
            losses = r["losses"]
            n = len(losses)
            n_fifth = max(n // 5, 1)
            first_20 = sum(losses[:n_fifth]) / n_fifth
            last_20 = sum(losses[-n_fifth:]) / n_fifth
            print(
                f"  Run {i}: first_20%={first_20:.1f}, "
                f"last_20%={last_20:.1f}"
            )
            assert last_20 < first_20, (
                f"Run {i}: loss did not decrease"
            )

    def test_task_dcm_posterior_finite(
        self, quick_results: list[dict],
    ) -> None:
        """Inferred A matrix contains only finite values."""
        assert len(quick_results) >= 1, "No successful runs"
        for r in quick_results:
            assert torch.isfinite(r["A_inferred"]).all(), (
                "NaN/Inf in inferred A matrix"
            )

    def test_task_dcm_diagonal_negative(
        self, quick_results: list[dict],
    ) -> None:
        """Inferred A diagonal is negative (self-inhibition)."""
        assert len(quick_results) >= 1, "No successful runs"
        for r in quick_results:
            diag = torch.diagonal(r["A_inferred"])
            assert (diag < 0).all(), (
                f"Non-negative diagonal in A: {diag}"
            )


