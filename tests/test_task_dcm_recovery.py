class TestTaskDCMRecoverySlow:
    """Task DCM parameter recovery -- slow validation tests.

    Strict thresholds with sufficient data and SVI convergence.
    Each test runs independently without shared fixtures.
    """

    @pytest.mark.slow
    def test_task_dcm_recovery_10_datasets(self) -> None:
        """10 datasets, 3 regions, 1000 steps, 90s duration.

        Validates RMSE < 0.05, coverage in [0.80, 0.99],
        correlation > 0.85.
        """
        results = []
        for seed in range(100, 110):
            result = run_single_task_recovery(
                seed=seed, n_regions=3, num_steps=1000,
                duration=90.0, n_blocks=3,
            )
            if result is not None:
                results.append(result)
                rmse = compute_rmse_A(
                    result["A_true"], result["A_inferred"],
                )
                print(f"  seed={seed}: RMSE={rmse:.4f}")
            else:
                print(f"  seed={seed}: FAILED")

        assert len(results) >= 5, (
            f"Too few runs: {len(results)}/10"
        )

        # RMSE
        rmses = [
            compute_rmse_A(r["A_true"], r["A_inferred"])
            for r in results
        ]
        mean_rmse = sum(rmses) / len(rmses)
        print(f"Mean RMSE: {mean_rmse:.4f}")
        assert mean_rmse < 0.05, (
            f"Mean RMSE {mean_rmse:.4f} >= 0.05"
        )

        # Coverage
        coverage = compute_coverage(results)
        print(f"Coverage: {coverage:.4f}")
        assert 0.80 <= coverage <= 0.99, (
            f"Coverage {coverage:.4f} not in [0.80, 0.99]"
        )

        # Correlation
        all_true = torch.cat(
            [r["A_true"].flatten() for r in results]
        )
        all_inferred = torch.cat(
            [r["A_inferred"].flatten() for r in results]
        )
        corr = _pearson_corr(all_true, all_inferred)
        print(f"Correlation: {corr:.4f}")
        assert corr > 0.85, f"Correlation {corr:.4f} <= 0.85"

