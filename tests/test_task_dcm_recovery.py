"""Parameter recovery tests for task-based DCM (REC-01).

Validates that Pyro SVI inference correctly recovers known ground-truth
connectivity parameters (A matrix) from synthetic BOLD data generated by
the task-DCM simulator. Tests use the simulate -> infer -> compare protocol.

CI-fast tests verify the pipeline works correctly (loss decreases, posterior
extractable, no NaN) on a few quick datasets. Strict recovery thresholds
(RMSE < 0.05, coverage in [0.80, 0.99], correlation > 0.85) are tested
in slow validation tests that use longer simulation and more SVI steps.

Task DCM SVI is computationally expensive due to ODE integration in each
SVI step (~1-2s/step on CPU for 3-region networks). CI tests use reduced
parameters for feasibility.

References
----------
[REF-001] Friston, Harrison & Penny (2003), Eq. 1.
[REF-002] Stephan et al. (2007), Eq. 2-6.
"""

from __future__ import annotations

import pytest
import torch
import pyro

from pyro_dcm.forward_models.neural_state import parameterize_A
from pyro_dcm.models import (
    task_dcm_model,
    create_guide,
    run_svi,
    extract_posterior_params,
)
from pyro_dcm.simulators.task_simulator import (
    make_block_stimulus,
    make_random_stable_A,
    simulate_task_dcm,
)


